''' In this programm we see the Data Types in python and also print the each DataTypes'''


'''
Text type- String
Numeric type- Int, Float, Complex
Sequence type- List, Tuple, Range
Mapping type- Dictionary
Boolean type- Boolean
Set type- Set, Frone set
Binary type- bytes, bytearray, memory
'''
# String
print("String: ", type("Hello, World!"))
# Int
print("Int: ", type(10))
# Float
print("Float: ", type(10.5))
# Complex
print("Complex: ", type(10 + 5j))
# List
print("List: ", type([1, 2, 3]))
# Tuple
print("Tuple: ", type((1,2,3)))
# Range
print("Range: ", type(range(1, 10)))
# Dictionary 
print("Dictionary: ", type({"name": "John", "age": 30}))        
# Boolean
print("Boolean: ", type(True))
# Set
print("Set: ", type({1, 2, 3}))
# Frozenset
print("Frozenset: ", type(frozenset({1, 2,})))
# Bytes
print("Bytes: ", type(b'Hello'))
# bytearray
print("bytearray: ", type(bytearray(b'Hello')))


